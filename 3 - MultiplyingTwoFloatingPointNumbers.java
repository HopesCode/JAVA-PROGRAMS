package hopescode_java_programs;

public class MultiplyingTwoFloatingPointNumbers { 
    public static void main(String[] args) {
        //we declaring and initializing values to 2 float datatype variables
        //float data type take those values that carries point
        float a = 12.5f ;
        float b = 2.2f ;
        //here we total the ammount because both A and B variable values of floating data type 
        float c = a * b ;
        //and here we print the result
        System.out.println("Multiplying 2 Floating point values : "+c);
    }
}
